"""
ml/training/train_placer.py

Trains the ML luminaire placement classifier.

The model predicts luminaire TYPE (A / AW / C / E) for each candidate
position generated by ceiling_grid.  It replaces the rule-based wall-relation
logic in real_placer._place_sales().

Data sources (combinable):
  --bootstrap    Run reference DXF files through the geometric pipeline and
                 save the outputs as training samples (uses current rules as
                 teacher).  This is the first-time bootstrap.
  --annotations  Load JSONL file written by real_placer at inference time
                 (path: data/annotations/placements.jsonl).
  --synthetic    Generate synthetic samples from known feature distributions
                 when real data is scarce (<200 samples).

Usage:
  python ml/training/train_placer.py --bootstrap
  python ml/training/train_placer.py --annotations data/annotations/placements.jsonl
  python ml/training/train_placer.py --bootstrap --annotations data/annotations/placements.jsonl
"""
