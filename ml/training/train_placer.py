# ── Bootstrap: run geometric pipeline and collect (features, type) pairs ─────

def bootstrap_from_dwg(dwg_dir: Path = DWG_DIR,
                       output_jsonl: Path = PLACEMENTS_JSONL) -> tuple:
    """
    Run the geometric pipeline on all DXF files in dwg_dir and save the
    resulting (feature_vector, luminaire_type) pairs as training samples.

    Returns (X, y) numpy arrays.
    """
    import io, contextlib
    from main import run_pipeline

    # Patch real_placer to intercept placed luminaires with features
    import services.placer.real_placer as rp

    samples: list[dict] = []

    orig_place_sales = rp.RealLuminairePlacer._place_sales

    def patched_place_sales(self, zone, plan, excl, ox, oy):
        result = orig_place_sales(self, zone, plan, excl, ox, oy)
        # After placement, re-extract features from training_buffer if populated
        buf = getattr(self, '_training_buffer', [])
        samples.extend(buf)
        self._training_buffer = []
        return result

    rp.RealLuminairePlacer._place_sales = patched_place_sales

    dxf_files = sorted(dwg_dir.glob("*_converted.dxf"))
    # Prefer the most recent copy of each unique store name
    seen: dict[str, Path] = {}
    for f in dxf_files:
        # Strip the leading hash prefix to get the canonical name
        parts = f.stem.split("_", 1)
        key = parts[1] if len(parts) > 1 else f.stem
        seen[key] = f
    targets = list(seen.values())

    print(f"Bootstrap: processing {len(targets)} DXF file(s) …")
    for dxf in targets:
        print(f"  → {dxf.name}")
        buf = io.StringIO()
        try:
            with contextlib.redirect_stdout(buf):
                run_pipeline(str(dxf))
        except Exception as e:
            print(f"    WARN: {e}")

    rp.RealLuminairePlacer._place_sales = orig_place_sales

    if not samples:
        print("  No training samples collected (placer training hooks not active).")
        print("  Hint: set COLLECT_TRAINING=1 before bootstrapping.")
        return np.empty((0, FEATURE_DIM)), []

    print(f"  Collected {len(samples)} placement samples.")

    output_jsonl.parent.mkdir(parents=True, exist_ok=True)
    with open(output_jsonl, 'a') as f:
        for s in samples:
            f.write(json.dumps(s) + "\n")
    print(f"  Saved → {output_jsonl}")

    return _jsonl_to_arrays([output_jsonl])


def _jsonl_to_arrays(paths) -> tuple:
    X, y = [], []
    for path in paths:
        p = Path(path)
        if not p.exists():
            continue
        with open(p) as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                obj = json.loads(line)
                fv  = obj.get("features")
                lt  = obj.get("lumi_type")
                if fv and lt and lt in LUMI_TYPES:
                    X.append(fv)
                    y.append(lt)
    return (np.array(X, dtype=np.float32) if X else np.empty((0, FEATURE_DIM), dtype=np.float32)), y


