# ── Training ──────────────────────────────────────────────────────────────────

def train(X: np.ndarray, y: list, save: bool = True) -> RandomForestClassifier:
    from collections import Counter
    counts = Counter(y)
    print(f"\nTraining placement model: {len(X)} samples")
    print(f"  Classes: {dict(counts)}")

    clf = RandomForestClassifier(
        n_estimators=500,
        max_depth=18,
        min_samples_leaf=2,
        max_features='sqrt',
        class_weight='balanced',
        random_state=42,
        n_jobs=-1,
    )

    if len(X) >= 20 and len(set(y)) >= 2:
        n_splits = min(5, min(counts.values()))
        if n_splits >= 2:
            cv = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=42)
            scores = cross_val_score(clf, X, y, cv=cv, scoring='f1_macro')
            print(f"  CV F1-macro: {scores.mean():.3f} ± {scores.std():.3f}")

    clf.fit(X, y)

    if len(X) >= 40 and len(set(y)) >= 2:
        from sklearn.model_selection import train_test_split
        try:
            Xtr, Xte, ytr, yte = train_test_split(X, y, test_size=0.2,
                                                    stratify=y, random_state=99)
            eval_clf = RandomForestClassifier(
                n_estimators=500, max_depth=18, min_samples_leaf=2,
                class_weight='balanced', random_state=42, n_jobs=-1)
            eval_clf.fit(Xtr, ytr)
            print("\nHold-out report:\n" + classification_report(yte, eval_clf.predict(Xte)))
        except ValueError:
            pass   # too few samples for stratified split

    if save:
        MODELS_DIR.mkdir(parents=True, exist_ok=True)
        with open(PLACER_MODEL_PATH, 'wb') as f:
            pickle.dump(clf, f)
        print(f"\nModel saved → {PLACER_MODEL_PATH}")

    return clf


def feature_importance(clf: RandomForestClassifier):
    FEAT_NAMES = [
        'dist_to_boundary_m', 'dist_to_gondola_m', 'dist_to_column_m',
        'is_wall_tagged', 'norm_x', 'norm_y', 'zone_area_log',
        'shelving_density', 'checkout_norm', 'zone_type_enc',
        'is_near_column', 'boundary_log', 'gondola_log',
        'tile_phase_x', 'tile_phase_y', 'n_shelving_norm',
        'shelf_depth_norm', 'is_wall_assortment', 'is_large_depth',
    ]
    ranked = sorted(zip(FEAT_NAMES, clf.feature_importances_),
                    key=lambda x: x[1], reverse=True)
    print("\nFeature importances:")
    for name, imp in ranked:
        print(f"  {name:<26} {imp:.4f}  {'█' * int(imp * 60)}")


# ── CLI ───────────────────────────────────────────────────────────────────────

if __name__ == '__main__':
    ap = argparse.ArgumentParser(description="Train placement classifier")
    ap.add_argument('--bootstrap',   action='store_true',
                    help='Run DXF files through geometric pipeline and collect samples')
    ap.add_argument('--annotations', default=str(PLACEMENTS_JSONL),
                    help='JSONL file with saved placement samples')
    ap.add_argument('--synthetic',   action='store_true',
                    help='Add synthetic samples when real data is scarce')
    ap.add_argument('--n-synthetic', type=int, default=400,
                    help='Synthetic samples per class')
    ap.add_argument('--no-save',     action='store_true')
    args = ap.parse_args()

    X_all = np.empty((0, FEATURE_DIM), dtype=np.float32)
    y_all: list = []

    if args.bootstrap:
        Xb, yb = bootstrap_from_dwg()
        if len(Xb):
            X_all = np.vstack([X_all, Xb]) if len(X_all) else Xb
            y_all.extend(yb)

    Xa, ya = _jsonl_to_arrays([args.annotations])
    if len(Xa):
        X_all = np.vstack([X_all, Xa]) if len(X_all) else Xa
        y_all.extend(ya)

    if args.synthetic or len(X_all) < 200:
        print(f"Adding synthetic samples ({args.n_synthetic}/class) …")
        Xs, ys = generate_synthetic(args.n_synthetic)
        X_all = np.vstack([X_all, Xs]) if len(X_all) else Xs
        y_all.extend(ys)

    if len(X_all) == 0:
        print("No training data. Use --synthetic or --bootstrap.")
        sys.exit(1)

    clf = train(X_all, y_all, save=not args.no_save)
    feature_importance(clf)
    print(f"\nDone. {len(X_all)} samples total.")
